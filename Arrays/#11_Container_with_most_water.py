class Solution:
    def maxArea(self, heights: List[int]) -> int:
        l, r = 0, len(heights) - 1
        res = 0

        while l < r:
            area = min(heights[l], heights[r]) * (r - l)
            res = max(res, area)
            if heights[l] <= heights[r]:
                l += 1
            else:
                r -= 1
        return res
    
# Time Complexity: O(n)
# Space Complexity: O(1)
# Using two pointer approach to find the maximum area that can be formed between two lines represented by the heights array.